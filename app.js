const elements = {
  pillRow: document.getElementById("pillRow"),
  chemicalsList: document.getElementById("chemicalsList"),
  foodList: document.getElementById("foodList"),
  machineryList: document.getElementById("machineryList"),
};

const safeStorage = {
  get(key) {
    try {
      return localStorage.getItem(key);
    } catch {
      return null;
    }
  },
  set(key, value) {
    try {
      localStorage.setItem(key, value);
    } catch {
      return null;
    }
  },
};

const setLanguage = (lang) => {
  const data = translations[lang] || translations.en;
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    node.textContent = data[key] || "";
  });

  elements.pillRow.innerHTML = data.pills.map((item) => `<span class="pill">${item}</span>`).join("");
  elements.chemicalsList.innerHTML = data.chemicals.map((item) => `<li>${item}</li>`).join("");
  elements.foodList.innerHTML = data.food.map((item) => `<li>${item}</li>`).join("");
  elements.machineryList.innerHTML = data.machinery.map((item) => `<li>${item}</li>`).join("");

  document.querySelectorAll(".lang-switch button").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
    button.setAttribute("aria-pressed", button.dataset.lang === lang ? "true" : "false");
  });

  safeStorage.set("polar-sinergy-lang", lang);
};

document.querySelectorAll(".lang-switch button").forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

const savedLang = safeStorage.get("polar-sinergy-lang");
const browserLang = navigator.language?.slice(0, 2).toLowerCase();
setLanguage(translations[savedLang] ? savedLang : translations[browserLang] ? browserLang : "en");

const revealNodes = document.querySelectorAll(".reveal");
const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.16 }
);

revealNodes.forEach((node) => revealObserver.observe(node));
